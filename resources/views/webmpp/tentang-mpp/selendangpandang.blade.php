            <p>Dibangunnya Mal Pelayanan Publik merupakan bukti keseriusan Pemerintah Daerah Kabupaten Tulang Bawang dalam mendorong percepatan pembangunan daerah. Terbangunnya Mal Pelayanan Publik akan membentuk sinergitas positif antara Pemerintah Daerah, masyarakat dan sektor swasta (dunia usaha).</p>
            <p>Mal Pelayanan Publik adalah tempat berlangsungnya kegiatan atau aktivitas penyelenggaraan pelayanan publik atas barang, jasa dan/atau pelayanan administrasi yang merupakan perluasan fungsi pelayanan terpadu baik pusat maupun daerah, serta pelayanan Badan Usaha Milik Negara/Badan Usaha Milik Daerah dan Swasta dalam rangka menyediakan pelayanan yang cepat, mudah, terjangkau, aman dan nyaman.</p>
            <h4>Tujuan Mal Pelayanan Publik:</h4>
            <ul>
                <li>Memberikan kemudahan, kecepatan, keterjangkauan, keamanan dan kenyamanan kepada masyarakat dalam mendapatkan pelayanan.</li>
                <li>Meningkatkan daya saing global dalam memberikan kemudahan berusaha di Indonesia.</li>
            </ul>
            <h4>Mal Pelayanan Publik Kabupaten Tulang Bawang</h4>
            <ul>
                <li>Soft Launching MPP Kabupaten Tulang Bawang dilaksanakan pada hari Kamis, tanggal 14 Maret 2019 yang dihadiri oleh Anggota DPR RI Azis Syamsuddin.</li>
                <li>Berlokasi di Jalan Lintas Timur Km. 112 Tiuh Tohou Kec Menggala Kabupaten Tulang Bawang.</li>
            </ul>
            <h4>Jenis Layanan</h4>
            <p>Jenis Layanan yang terdapat di Mal Pelayanan Publik Kabupaten Tulang Bawang sebanyak 159 Jenis Layanan dari 12 Satker di lingkup Pemkab Tulang Bawang dan 5 Satker vertikal yang terdiri dari:</p>
            <ol type="1">
                <li>DPMPTSP (Seluruh Perizinan melalui OSS, Perizinan melalui Aplikasi Sicantik).</li>
                <li>Disdukcapil (Biodata Penduduk, KK, Perekaman dan Pencetakan KTP, dll.).</li>
                <li>Dinas Pendidikan (Izin Operasional Satuan Pendidikan, Izin Pendirian Satuan Pendidikan).</li>
                <li>Dinas Kesehatan (Rekomendasi pendirian RS, Klinik, Apotek, dll.).</li>
                <li>Dinas Koperasi dan UKM (Rekom Surat Izin Usaha, Rekom Pengesahan Badan Hukum Koperasi, dll.).</li>
                <li>Dinas Lingkungan Hidup (Amdal, UKL, UPL, SPPL, IPLC, dll.).</li>
                <li>Badan Kesatuan Bangsa dan Politik Daerah (Izin Penelitian, KKN/PKL, dll.).</li>
                <li>Dinas Pekerjaan Umum dan Penataan Ruang (Rekom IMB).</li>
                <li>Dinas Pariwisata (Tanda Daftar Usaha Pariwisata).</li>
                <li>Dinas Komunikasi dan Informatika (Rekom Pendirian Menara Telekomunikasi).</li>
                <li>Dinas Tenaga Kerja dan Transmigrasi (Kartu Rekam Paspor TKI, BPJS Ketenagakerjaan, dll.).</li>
                <li>Dinas Pengelolaan dan Pendapatan Daerah.</li>
                <li>Polres (Sidik Jari dan SKCK).</li>
                <li>Kejari (Konsultasi Layanan Hukum).</li>
                <li>Kemenag (Pengambilan Perangkat Administrasi NR, Legaliser, Buku Nikah, dll.).</li>
                <li>KP2KP (Pendaftaran NPWP, Pembuatan ID Billing, Konsultasi Perpajakan, dll.).</li>
                <li>BPOM (Konsultasi dan Pengaduan terkait Obat dan Makanan).</li>
                <li>Bank Lampung.</li>
                <li>Bank Mandiri.</li>
            </ol>
